/*
The java string length() method length of the string. It returns count of total number of characters.
The length of java string is same as the unicode code units of the string.

*/
public class length
{  
public static void main(String args[])
{  
String s1="github";  
String s2="java";  
System.out.println("string length is: "+s1.length());//string length is: 6 
System.out.println("string length is: "+s2.length());//string length is: 4  
}
}  
